int	ft_atoi(char *str)
{
	int	n;
	int	negative;
	
	if(str[1] == '-')
		negative=1;
	n = 0;
	while (*str >= '0' && *str <= '9')
		n = n * 10 + *str++ - '0';
	if(negative)
	{
		n = n * -1;
	}
	return (n);
}