const char	*ft_strrchr(const char *s, int c)
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (c <0)
		return (s);
	while (s[i])
	{
		if (s[i] == (char)c)
		{
			j=i;
		}
		i++;
	}
	return (s[j]);
}
